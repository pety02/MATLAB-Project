% Интервал за t
t = 1:0.1:16;

% Интервал за x
x = 0:0.05:8;

% Изобразяване на движението на струна
for k = 1:length(t)
    % Пълно решение на задачата за струната
    u = (phi(x + t(k)) + phi(x - t(k))) / 2 + intpsi(x - t(k),x + t(k)) / 2;
    plot(x, u);
    
    % Интервал на изобразяване
    axis([0,8,-4,4]);

    % Анимация на диаграмата
    M(k) = getframe;
end

% Повторение на анимацията 3 пъти
movie(M,3);